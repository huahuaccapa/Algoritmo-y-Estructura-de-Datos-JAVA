    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombreUsuario = jTextField1.getText().toLowerCase();
        String contrasena = new String(jPasswordField2.getPassword()).toLowerCase();

        Usuario usuario = new Usuario(nombreUsuario, contrasena);

        UsuarioDAOImpl usuarioDAO = new UsuarioDAOImpl();

        if (usuarioDAO.validarUsuario(usuario)) {
            JOptionPane.showMessageDialog(this, "¡Bienvenido!", "Login Exitoso", JOptionPane.INFORMATION_MESSAGE);

            this.setVisible(false);

            FormularioPrincipal principal = new FormularioPrincipal();
            principal.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos.", "Error de Login", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

